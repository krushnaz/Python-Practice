num = int(input("enter a number : "));

if(num == 1) :
    print("sunday");
elif(num == 2) :
    print("monday");
elif(num == 3) :
    print("thuesday");
elif(num == 4) :
    print("wednesday");
elif(num == 5) :
    print("thursday");
elif(num == 6) :
    print("friday");
elif(num == 7) :
    print("saturday");
else :
    print("invalid choice!");   